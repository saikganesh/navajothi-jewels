
import React, { useState, useEffect } from 'react';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { Textarea } from '@/components/ui/textarea';
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from '@/components/ui/select';
import { Table, TableBody, TableCell, TableHead, TableHeader, TableRow } from '@/components/ui/table';
import { Badge } from '@/components/ui/badge';
import { Dialog, DialogContent, DialogDescription, DialogHeader, DialogTitle } from '@/components/ui/dialog';
import { Plus, Edit, Trash2 } from 'lucide-react';
import { supabase } from '@/integrations/supabase/client';
import { useToast } from '@/hooks/use-toast';
import CategoryManagement from './CategoryManagement';
import CollectionManagement from './CollectionManagement';

interface Product {
  id: string;
  name: string;
  description: string | null;
  collection_id: string | null;
  gross_weight: number | null;
  stone_weight: number | null;
  net_weight: number | null;
  carat: '22ct' | '18ct' | null;
  images: string[] | null;
  in_stock: boolean;
  price: number | null;
  created_at: string;
  updated_at: string;
  collections?: {
    name: string;
    categories?: {
      name: string;
    };
  };
}

interface Collection {
  id: string;
  name: string;
  category_id: string;
  categories?: {
    name: string;
  };
}

const ProductsManagement = () => {
  const [products, setProducts] = useState<Product[]>([]);
  const [collections, setCollections] = useState<Collection[]>([]);
  const [isLoading, setIsLoading] = useState(true);
  const [showAddForm, setShowAddForm] = useState(false);
  const [editingProduct, setEditingProduct] = useState<Product | null>(null);
  const [autoGeneratedId, setAutoGeneratedId] = useState<string>('');
  const { toast } = useToast();

  const [formData, setFormData] = useState({
    name: '',
    description: '',
    collection_id: '',
    gross_weight: '',
    stone_weight: '',
    carat: '',
    images: '',
  });

  useEffect(() => {
    fetchProducts();
    fetchCollections();
  }, []);

  useEffect(() => {
    // Generate a new UUID when the form opens
    if (showAddForm && !editingProduct) {
      setAutoGeneratedId(crypto.randomUUID());
    }
  }, [showAddForm, editingProduct]);

  const fetchProducts = async () => {
    try {
      const { data, error } = await supabase
        .from('products')
        .select(`
          *,
          collections (
            name,
            categories (
              name
            )
          )
        `)
        .order('created_at', { ascending: false });

      if (error) throw error;
      
      // Transform the data to ensure images is always a string array and handle type casting
      const transformedProducts = (data || []).map(product => ({
        ...product,
        images: Array.isArray(product.images) 
          ? product.images.map(img => String(img)) 
          : product.images 
            ? [String(product.images)]
            : []
      })) as Product[];
      
      setProducts(transformedProducts);
    } catch (error) {
      console.error('Error fetching products:', error);
      toast({
        title: "Error",
        description: "Failed to load products",
        variant: "destructive",
      });
    } finally {
      setIsLoading(false);
    }
  };

  const fetchCollections = async () => {
    try {
      const { data, error } = await supabase
        .from('collections')
        .select(`
          id,
          name,
          category_id,
          categories (
            name
          )
        `)
        .order('name');

      if (error) throw error;
      setCollections(data || []);
    } catch (error) {
      console.error('Error fetching collections:', error);
    }
  };

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    
    try {
      const productData = {
        id: editingProduct ? editingProduct.id : autoGeneratedId,
        name: formData.name,
        description: formData.description || null,
        collection_id: formData.collection_id || null,
        gross_weight: formData.gross_weight ? parseFloat(formData.gross_weight) : null,
        stone_weight: formData.stone_weight ? parseFloat(formData.stone_weight) : null,
        carat: formData.carat as '22ct' | '18ct' | null || null,
        images: formData.images ? formData.images.split(',').map(url => url.trim()).filter(url => url) : [],
        in_stock: true,
        price: null, // Set price to null since we removed the field
      };

      if (editingProduct) {
        const { error } = await supabase
          .from('products')
          .update({ ...productData, updated_at: new Date().toISOString() })
          .eq('id', editingProduct.id);

        if (error) throw error;
        
        toast({
          title: "Success",
          description: "Product updated successfully",
        });
      } else {
        const { error } = await supabase
          .from('products')
          .insert([productData]);

        if (error) throw error;

        toast({
          title: "Success",
          description: "Product added successfully",
        });
      }

      resetForm();
      fetchProducts();
    } catch (error) {
      console.error('Error saving product:', error);
      toast({
        title: "Error",
        description: "Failed to save product",
        variant: "destructive",
      });
    }
  };

  const handleEdit = (product: Product) => {
    setEditingProduct(product);
    setFormData({
      name: product.name,
      description: product.description || '',
      collection_id: product.collection_id || '',
      gross_weight: product.gross_weight?.toString() || '',
      stone_weight: product.stone_weight?.toString() || '',
      carat: product.carat || '',
      images: product.images?.join(', ') || '',
    });
    setShowAddForm(true);
  };

  const handleDelete = async (productId: string) => {
    if (!confirm('Are you sure you want to delete this product?')) return;

    try {
      const { error } = await supabase
        .from('products')
        .delete()
        .eq('id', productId);

      if (error) throw error;

      toast({
        title: "Success",
        description: "Product deleted successfully",
      });
      
      fetchProducts();
    } catch (error) {
      console.error('Error deleting product:', error);
      toast({
        title: "Error",
        description: "Failed to delete product",
        variant: "destructive",
      });
    }
  };

  const resetForm = () => {
    setFormData({
      name: '',
      description: '',
      collection_id: '',
      gross_weight: '',
      stone_weight: '',
      carat: '',
      images: '',
    });
    setShowAddForm(false);
    setEditingProduct(null);
    setAutoGeneratedId('');
  };

  // Calculate net weight
  const netWeight = formData.gross_weight && formData.stone_weight 
    ? (parseFloat(formData.gross_weight) - parseFloat(formData.stone_weight)).toFixed(3)
    : formData.gross_weight || '';

  if (isLoading) {
    return (
      <div className="space-y-6">
        <div>
          <h2 className="text-3xl font-bold tracking-tight">Products Management</h2>
          <p className="text-muted-foreground">Loading products...</p>
        </div>
      </div>
    );
  }

  return (
    <div className="space-y-6">
      <div className="flex items-center justify-between">
        <div>
          <h2 className="text-3xl font-bold tracking-tight">Products Management</h2>
          <p className="text-muted-foreground">
            Manage your jewelry collection
          </p>
        </div>
        <div className="flex gap-2">
          <CategoryManagement />
          <CollectionManagement />
          <Button onClick={() => setShowAddForm(true)}>
            <Plus className="h-4 w-4 mr-2" />
            Add Product
          </Button>
        </div>
      </div>

      <Dialog open={showAddForm} onOpenChange={setShowAddForm}>
        <DialogContent className="max-w-2xl max-h-[90vh] overflow-y-auto">
          <DialogHeader>
            <DialogTitle>{editingProduct ? 'Edit Product' : 'Add New Product'}</DialogTitle>
            <DialogDescription>
              {editingProduct ? 'Update product information' : 'Fill in the details to add a new product'}
            </DialogDescription>
          </DialogHeader>
          <form onSubmit={handleSubmit} className="space-y-4">
            <div className="grid grid-cols-1 md:grid-cols-2 gap-4">
              <div>
                <Label htmlFor="product-id">Product ID</Label>
                <Input
                  id="product-id"
                  value={editingProduct?.id || autoGeneratedId}
                  disabled
                  className="bg-muted"
                />
              </div>
              <div>
                <Label htmlFor="name">Product Name</Label>
                <Input
                  id="name"
                  value={formData.name}
                  onChange={(e) => setFormData({ ...formData, name: e.target.value })}
                  required
                />
              </div>
              <div>
                <Label htmlFor="collection">Collection</Label>
                <Select
                  value={formData.collection_id}
                  onValueChange={(value) => setFormData({ ...formData, collection_id: value })}
                >
                  <SelectTrigger>
                    <SelectValue placeholder="Select collection" />
                  </SelectTrigger>
                  <SelectContent>
                    {collections.map((collection) => (
                      <SelectItem key={collection.id} value={collection.id}>
                        {collection.name} ({collection.categories?.name})
                      </SelectItem>
                    ))}
                  </SelectContent>
                </Select>
              </div>
              <div>
                <Label htmlFor="gross_weight">Gross Weight (g)</Label>
                <Input
                  id="gross_weight"
                  type="number"
                  step="0.001"
                  value={formData.gross_weight}
                  onChange={(e) => setFormData({ ...formData, gross_weight: e.target.value })}
                  placeholder="0.000"
                />
              </div>
              <div>
                <Label htmlFor="stone_weight">Stone Weight (g)</Label>
                <Input
                  id="stone_weight"
                  type="number"
                  step="0.001"
                  value={formData.stone_weight}
                  onChange={(e) => setFormData({ ...formData, stone_weight: e.target.value })}
                  placeholder="0.000"
                />
              </div>
              <div>
                <Label htmlFor="net_weight">Net Weight (g)</Label>
                <Input
                  id="net_weight"
                  value={netWeight}
                  disabled
                  className="bg-muted"
                  placeholder="Auto-calculated"
                />
              </div>
              <div>
                <Label htmlFor="carat">Carat</Label>
                <Select
                  value={formData.carat}
                  onValueChange={(value) => setFormData({ ...formData, carat: value })}
                >
                  <SelectTrigger>
                    <SelectValue placeholder="Select carat" />
                  </SelectTrigger>
                  <SelectContent>
                    <SelectItem value="22ct">22ct</SelectItem>
                    <SelectItem value="18ct">18ct</SelectItem>
                  </SelectContent>
                </Select>
              </div>
            </div>
            <div>
              <Label htmlFor="description">Description</Label>
              <Textarea
                id="description"
                value={formData.description}
                onChange={(e) => setFormData({ ...formData, description: e.target.value })}
                placeholder="Product description..."
              />
            </div>
            <div>
              <Label htmlFor="images">Image URLs (comma-separated)</Label>
              <Textarea
                id="images"
                value={formData.images}
                onChange={(e) => setFormData({ ...formData, images: e.target.value })}
                placeholder="https://image1.jpg, https://image2.jpg"
              />
            </div>
            <div className="flex space-x-2">
              <Button type="submit">
                {editingProduct ? 'Update Product' : 'Add Product'}
              </Button>
              <Button type="button" variant="outline" onClick={resetForm}>
                Cancel
              </Button>
            </div>
          </form>
        </DialogContent>
      </Dialog>

      <Card>
        <CardHeader>
          <CardTitle>Products ({products.length})</CardTitle>
          <CardDescription>
            Manage your jewelry inventory
          </CardDescription>
        </CardHeader>
        <CardContent>
          <Table>
            <TableHeader>
              <TableRow>
                <TableHead>Product ID</TableHead>
                <TableHead>Name</TableHead>
                <TableHead>Category</TableHead>
                <TableHead>Gross Weight</TableHead>
                <TableHead>Carat</TableHead>
                <TableHead>Actions</TableHead>
              </TableRow>
            </TableHeader>
            <TableBody>
              {products.map((product) => (
                <TableRow key={product.id}>
                  <TableCell className="font-mono text-xs">{product.id.slice(0, 8)}...</TableCell>
                  <TableCell className="font-medium">{product.name}</TableCell>
                  <TableCell>{product.collections?.categories?.name || '-'}</TableCell>
                  <TableCell>{product.gross_weight ? `${product.gross_weight}g` : '-'}</TableCell>
                  <TableCell>{product.carat || '-'}</TableCell>
                  <TableCell>
                    <div className="flex space-x-2">
                      <Button
                        size="sm"
                        variant="outline"
                        onClick={() => handleEdit(product)}
                      >
                        <Edit className="h-4 w-4" />
                      </Button>
                      <Button
                        size="sm"
                        variant="outline"
                        onClick={() => handleDelete(product.id)}
                      >
                        <Trash2 className="h-4 w-4" />
                      </Button>
                    </div>
                  </TableCell>
                </TableRow>
              ))}
              {products.length === 0 && (
                <TableRow>
                  <TableCell colSpan={6} className="text-center text-muted-foreground">
                    No products found. Add your first product to get started.
                  </TableCell>
                </TableRow>
              )}
            </TableBody>
          </Table>
        </CardContent>
      </Card>
    </div>
  );
};

export default ProductsManagement;
